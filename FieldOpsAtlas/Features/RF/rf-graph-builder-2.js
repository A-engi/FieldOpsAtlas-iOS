  function clamp(value, minimum, maximum) {
    return Math.max(
      minimum,
      Math.min(maximum, value)
    );
  }

  function decodeBase64(encoded) {
    const binary = atob(encoded);
    const bytes = new Uint8Array(binary.length);

    for (let index = 0; index < binary.length; index += 1) {
      bytes[index] = binary.charCodeAt(index);
    }

    return bytes.buffer;
  }

  function decodeFloat32(encoded) {
    return new Float32Array(
      decodeBase64(encoded)
    );
  }

  let terrainShapeBounds = null;

  function getTerrainShapeBounds() {
    if (terrainShapeBounds) {
      return terrainShapeBounds;
    }

    const positions = decodeFloat32(
      EMBEDDED.meshPositions
    );

    let minimumY = Infinity;
    let maximumY = -Infinity;

    for (
      let offset = 0;
      offset < positions.length;
      offset += 3
    ) {
      const y = positions[offset + 1];

      minimumY = Math.min(minimumY, y);
      maximumY = Math.max(maximumY, y);
    }

    terrainShapeBounds = {
      minimumY,
      maximumY,
      height: Math.max(
        0.001,
        maximumY - minimumY
      )
    };

    return terrainShapeBounds;
  }

  function fract(value) {
    return value - Math.floor(value);
  }

  function trianglePulse(value) {
    return 1
      - Math.abs(
        fract(value) * 2 - 1
      );
  }

  function computeScaleFacet(x, z) {
    const facetScale = 1.52;
    const skewX = x * facetScale;
    const skewZ = z * facetScale;
    const basisA = skewX + skewZ * 0.5;
    const basisB = skewZ * 0.86602540378;
    const basisC = basisA - basisB;

    const edgeA = trianglePulse(basisA);
    const edgeB = trianglePulse(basisB);
    const edgeC = trianglePulse(basisC);

    const cellCore = Math.pow(
      Math.min(
        edgeA,
        edgeB,
        edgeC
      ),
      1.22
    );

    const cellRidge = Math.pow(
      Math.max(
        edgeA * edgeB,
        edgeB * edgeC,
        edgeC * edgeA
      ),
      1.02
    );

    return clamp(
      cellCore * 1.18
      + cellRidge * 0.50,
      0,
      1.38
    );
  }

  function computeRidgeFan(x, z) {
    const lanes = [
      {
        dx: 0.00,
        dz: 1.00,
        offset: 0.00,
        width: 0.56,
        weight: 1.00
      },
      {
        dx: 0.58,
        dz: 0.82,
        offset: 0.35,
        width: 0.62,
        weight: 0.92
      },
      {
        dx: -0.64,
        dz: 0.77,
        offset: -0.28,
        width: 0.68,
        weight: 0.88
      },
      {
        dx: 0.92,
        dz: 0.24,
        offset: 0.54,
        width: 0.74,
        weight: 0.72
      },
      {
        dx: -0.90,
        dz: 0.30,
        offset: -0.48,
        width: 0.76,
        weight: 0.68
      }
    ];

    let ridge = 0;

    for (
      let index = 0;
      index < lanes.length;
      index += 1
    ) {
      const lane = lanes[index];
      const along =
        x * lane.dx
        + z * lane.dz;

      const pulse = trianglePulse(
        along * lane.width + lane.offset
      );

      ridge = Math.max(
        ridge,
        Math.pow(pulse, 4.2)
        * lane.weight
      );
    }

    return ridge;
  }

  function shapeTerrainPositions(encoded) {
    const positions = decodeFloat32(encoded);
    const bounds = getTerrainShapeBounds();

    const peakAnchors = [
      {
        x: -1.10,
        z: -0.60,
        radius: 1.78,
        lift: 2.95,
        spire: 1.00
      },
      {
        x: 3.05,
        z: -1.70,
        radius: 1.78,
        lift: 1.78,
        spire: 0.72
      },
      {
        x: -7.65,
        z: 6.70,
        radius: 2.12,
        lift: 1.72,
        spire: 0.62
      },
      {
        x: 4.90,
        z: 8.55,
        radius: 1.92,
        lift: 1.52,
        spire: 0.58
      },
      {
        x: -4.15,
        z: 2.60,
        radius: 1.34,
        lift: 1.20,
        spire: 0.72
      },
      {
        x: 1.70,
        z: 2.85,
        radius: 1.28,
        lift: 1.12,
        spire: 0.68
      },
      {
        x: 5.85,
        z: 3.30,
        radius: 1.40,
        lift: 1.05,
        spire: 0.60
      }
    ];

    for (
      let offset = 0;
      offset < positions.length;
      offset += 3
    ) {
      const originalX = positions[offset];
      const originalY = positions[offset + 1];
      const originalZ = positions[offset + 2];

      const heightNorm = clamp(
        (
          originalY - bounds.minimumY
        ) / bounds.height,
        0,
        1
      );

      const upperRelief =
        Math.pow(heightNorm, 1.02)
        * (
          1 + heightNorm * 0.40
        );

      let localLift = 0;
      let spireLift = 0;
      let strongestInfluence = 0;
      let strongestAnchor = null;

      for (
        let anchorIndex = 0;
        anchorIndex < peakAnchors.length;
        anchorIndex += 1
      ) {
        const anchor =
          peakAnchors[anchorIndex];

        const deltaX =
          originalX - anchor.x;

        const deltaZ =
          originalZ - anchor.z;

        const influence = Math.exp(
          -(
            deltaX * deltaX
            + deltaZ * deltaZ
          ) / (
            2
            * anchor.radius
            * anchor.radius
          )
        );

        localLift +=
          influence
          * anchor.lift
          * Math.pow(heightNorm, 1.34);

        spireLift +=
          Math.pow(influence, 1.72)
          * anchor.spire
          * Math.pow(heightNorm, 1.92)
          * 0.82;

        if (
          influence
          > strongestInfluence
        ) {
          strongestInfluence = influence;
          strongestAnchor = anchor;
        }
      }

      const anchorPull =
        strongestAnchor
          ? Math.pow(
              strongestInfluence,
              1.18
            )
            * Math.pow(heightNorm, 1.72)
            * 0.185
          : 0;

      const mockupSpinePull =
        Math.pow(heightNorm, 1.46)
        * 0.050;

      const baseX =
        originalX
        + (
          strongestAnchor
            ? strongestAnchor.x
              - originalX
            : 0
        ) * anchorPull
        - originalX
          * mockupSpinePull
          * 0.15;

      const baseZ =
        originalZ
        + (
          strongestAnchor
            ? strongestAnchor.z
              - originalZ
            : 0
        ) * anchorPull
        - originalZ
          * mockupSpinePull
          * 0.05;

      const localX =
        strongestAnchor
          ? baseX - strongestAnchor.x
          : baseX;

      const localZ =
        strongestAnchor
          ? baseZ - strongestAnchor.z
          : baseZ;

      const facetPattern =
        computeScaleFacet(
          baseX,
          baseZ
        );

      const ridgeFan =
        computeRidgeFan(
          localX,
          localZ
        );

      const facetStrength =
        Math.pow(heightNorm, 1.12)
        * clamp(
          0.54
          + strongestInfluence * 0.78,
          0.54,
          1.24
        );

      const ridgeStrength =
        Math.pow(heightNorm, 1.02)
        * clamp(
          0.34
          + strongestInfluence * 1.32,
          0.34,
          1.44
        );

      const spikeLift =
        Math.max(
          0,
          facetPattern - 0.16
        )
        * facetStrength
        * 0.48;

      const ridgeLift =
        Math.pow(ridgeFan, 1.35)
        * ridgeStrength
        * 0.88;

      const planeBreakLift =
        Math.pow(
          facetPattern,
          1.45
        )
        * Math.pow(
          heightNorm,
          1.36
        )
        * 0.22;

      const shoulderLift =
        facetPattern
        * facetStrength
        * 0.08;

      let facetDirectionX =
        Math.sin(
          baseX * 2.20
          + baseZ * 1.10
        ) * 0.35;

      let facetDirectionZ =
        Math.cos(
          baseZ * 2.00
          - baseX * 1.20
        ) * 0.35;

      if (strongestAnchor) {
        facetDirectionX +=
          baseX - strongestAnchor.x;

        facetDirectionZ +=
          baseZ - strongestAnchor.z;
      }

      const facetDirectionLength = Math.hypot(
        facetDirectionX,
        facetDirectionZ
      ) || 1;

      const lateralFacetPush =
        Math.max(
          0,
          facetPattern - 0.20
        )
        * facetStrength
        * 0.12;

      const ridgeDirectionLength = Math.hypot(
        localX,
        localZ
      ) || 1;

      const ridgeLateralPull =
        Math.pow(ridgeFan, 1.30)
        * ridgeStrength
        * 0.105;

      positions[offset] =
        baseX
        + facetDirectionX
          / facetDirectionLength
          * lateralFacetPush
        - localX
          / ridgeDirectionLength
          * ridgeLateralPull;

      positions[offset + 1] =
        bounds.minimumY
        + upperRelief * bounds.height
        + localLift
        + spireLift
        + spikeLift
        + ridgeLift
        + planeBreakLift
        + shoulderLift;

      positions[offset + 2] =
        baseZ
        + facetDirectionZ
          / facetDirectionLength
          * lateralFacetPush
        - localZ
          / ridgeDirectionLength
          * ridgeLateralPull
          * 0.48;
    }

    return positions;
  }

